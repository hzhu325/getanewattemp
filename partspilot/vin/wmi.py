def lookup_wmi(vin: str) -> dict:
    """返回 {country, manufacturer}；查不到厂商时 manufacturer 为空串。"""
    wmi3 = vin[:3].upper()
    manufacturer = _WMI_TABLE.get(wmi3, "")
    country = _COUNTRY_BY_FIRST.get(vin[0].upper(), "")
    return {"country": country, "manufacturer": manufacturer, "wmi": wmi3}
